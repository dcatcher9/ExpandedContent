            BlueprintArchetype FeralChampionArchetype = Resources.GetBlueprint<BlueprintArchetype>("f68ca492c9c15e241ab73735fbd0fb9f");
            BlueprintArchetype PriestOfBalance = Resources.GetBlueprint<BlueprintArchetype>("a4560e3fb5d247d68fb1a2738fcc0855");
            BlueprintArchetype ClawOfTheFalseWyrmArchetype = Resources.GetModBlueprint<BlueprintArchetype>("ClawOfTheFalseWyrmArchetype");
            BlueprintArchetype SwornOfTheEldestArchetype = Resources.GetModBlueprint<BlueprintArchetype>("SwornOfTheEldestArchetype");

            BlueprintFeature HeavyMaceProficiency = Resources.GetBlueprint<BlueprintFeature>("3f18330d717ea0148b496ee8cc291a60");
            var NurgalIcon = AssetLoader.LoadInternal("Deities", "Icon_Nurgal.jpg");
            var NurgalFeature = Helpers.CreateBlueprint<BlueprintFeature>("NurgalFeature", (bp => {
                bp.SetName("Nurgal");
                bp.SetDescription("\nTitles: The Shining Scourge, Lord of Senseless War " +
                    "\nAlignment: Chaotic Evil   " +
                    "\nAreas of Concern: Deserts, Senseless warfare, The Sun   " +
                    "\nDomains: Chaos, Evil, Fire, Sun   " +
                    "\nSubdomains: Ash, Day, Demon, Smoke   " +
                    "\nFavoured Weapon: Heavy mace   " +
                    "\nHoly Symbol: Lion jaws around the sun   " +
                    "\nSacred Animal: Lion   " +
                    "\nNot all who worship the sun do so with joy and kindness in their hearts. Those who venerate the Shining Scourge worship out of fear or awe. Nurgal embodies the " +
                    "destructive aspects of the sun, and his minions walk without fear in the full light of day. His lower body is that of a golden lion with a draconic tail. His " +
                    "torso is deeply tanned and masculine, and he is rarely seen without a heavy mace, the head of which appears to be a miniature sun, held in one four-fingered, " +
                    "taloned hand. His head is that of a lion as well, and blinding light spews from his eyes and mouth. Nurgal’s faith was quite strong in ancient Azlant, and not a " +
                    "few Azlanti ruins bear evidence of this connection in carvings that depict a fanged mouth around a sun or the presence of lion-like monsters. Today, his worship " +
                    "has lessened, and is limited to the deserts of northern Garund, Qadira, and ruined Ninshabur. His Abyssal realm is known as Kuthan, an expansive region of " +
                    "alternating desert and dry savannas under a vast red sun that never moves from its noontime height above. Narrow gorges hundreds of miles long connect Kuthan to " +
                    "its neighboring realm, the Sea of Whispering Sands, where Nurgal’s lover, Areshkagal, used to rule before being banished to the Blood Clefts by her sister. Nurgal " +
                    "is also believed to be the half-brother of Socothbenoth, but this relationship is not friendly, and the two have been at war for as long as they both have existed.");
                bp.m_Icon = NurgalIcon;
                bp.Ranks = 1;
                bp.IsClassFeature = true;
                bp.HideInCharacterSheetAndLevelUp = false;
                bp.SetDisallowedArchetype(ClericClass, PriestOfBalance);
                bp.SetDisallowedArchetype(WarpriestClass, FeralChampionArchetype);
                bp.SetDisallowedArchetype(DreadKnightClass, ClawOfTheFalseWyrmArchetype);
                bp.SetDisallowedArchetype(InquistorClass, SwornOfTheEldestArchetype);
                bp.DisallowAngelfireApostle();
                bp.DisallowDarkSister();

                bp.Groups = new FeatureGroup[] { FeatureGroup.Deities };
                bp.AddComponent<PrerequisiteAlignment>(c => {
                    c.Alignment = AlignmentMaskType.ChaoticNeutral | AlignmentMaskType.NeutralEvil | AlignmentMaskType.ChaoticEvil;
                });
                bp.AddComponent<AddFacts>(c => {
                    c.m_Facts = new BlueprintUnitFactReference[] { 
                        ChannelNegativeAllowed.ToReference<BlueprintUnitFactReference>() 
                    };
                });
                bp.SetAllowedDomains(
                    DeityTools.DomainAllowed.ChaosDomainAllowed,
                    DeityTools.DomainAllowed.FireDomainAllowed,
                    DeityTools.DomainAllowed.EvilDomainAllowed,
                    DeityTools.DomainAllowed.SunDomainAllowed,
                    DeityTools.DomainAllowed.DemonDomainChaosAllowed,
                    DeityTools.DomainAllowed.DemonDomainEvilAllowed,
                    DeityTools.SeparatistDomainAllowed.AirDomainAllowedSeparatist,
                    DeityTools.SeparatistDomainAllowed.AnimalDomainAllowedSeparatist,
                    DeityTools.SeparatistDomainAllowed.ArtificeDomainAllowedSeparatist,
                    DeityTools.SeparatistDomainAllowed.CharmDomainAllowedSeparatist,
                    DeityTools.SeparatistDomainAllowed.CommunityDomainAllowedSeparatist,
                    DeityTools.SeparatistDomainAllowed.DarknessDomainAllowedSeparatist,
                    DeityTools.SeparatistDomainAllowed.DeathDomainAllowedSeparatist,
                    DeityTools.SeparatistDomainAllowed.DestructionDomainAllowedSeparatist,
