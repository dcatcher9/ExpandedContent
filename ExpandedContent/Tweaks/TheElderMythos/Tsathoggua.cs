                bp.SetDescription("\nTitles: Father of Night, Saint Toad, The Sleeper in the Deep   " +
                    "\nAlignment: Chaotic Evil   " +
                    "\nAreas of Concern: Magic, Outcasts, The Underworld   " +
                    "\nRank: Great Old One   " +
                    "\nDomains: Chaos, Evil, Knowledge, Magic   " +
                    "\nSubdomains: Arcane, Divine, Thought, Whimsy   " +
                    "\nFavoured Weapon: Shortsword   " +
                    "\nHoly Symbol: Crouching eyeless toad   " +
                    "\nTsathoggua dwells in the same network of vast caverns wherein can be found other divinities of the Elder Mythos, such as Abhoth, Atlach-Nacha, and Orgesh. Some maintain the " +
                    "specific underground realm ruled by Tsathoggua is a blue-lit realm called K’n-Yan, while others say the denizens of K’n-Yan rose up against Tsathoggua and drove him from that " +
                    "realm to a deeper lair where even light dreads to go—a dark tangle of caverns called N’kai. Tsathoggua has a strange, almost whimsical nature; while evil and unpredictable, " +
                    "at times he can appear almost benevolent in his dealings with mortals. Yet such dealings are never long-lived, and more often than not end in tragedy for those who would " +
                    "treat with the Great Old One. Tsathoggua appears as a semi-humanoid creature whose features mix those of a toad, a bat, and a sloth, with sleepy-looking eyes and a toothy " +
                    "grin.");
                bp.m_Icon = TsathogguaIcon;
                bp.Ranks = 1;
                bp.IsClassFeature = true;
                bp.HideInCharacterSheetAndLevelUp = false;
                bp.SetDisallowedArchetype(ClericClass, PriestOfBalance);
                bp.SetDisallowedArchetype(WarpriestClass, FeralChampionArchetype);
                bp.SetDisallowedArchetype(WarpriestClass, MantisZealotArchetype);
                bp.SetDisallowedArchetype(DreadKnightClass, ClawOfTheFalseWyrmArchetype);
                bp.SetDisallowedArchetype(InquistorClass, SwornOfTheEldestArchetype);
                bp.DisallowAngelfireApostle();
                bp.DisallowDarkSister();

                bp.Groups = new FeatureGroup[] { FeatureGroup.Deities };
                bp.AddComponent<PrerequisiteAlignment>(c => {
                    c.Alignment = AlignmentMaskType.ChaoticNeutral | AlignmentMaskType.NeutralEvil | AlignmentMaskType.ChaoticEvil;
                });
                bp.AddComponent<AddFacts>(c => {
                    c.m_Facts = new BlueprintUnitFactReference[] { 
                        ChannelNegativeAllowed.ToReference<BlueprintUnitFactReference>() 
                    };
                });
                bp.SetAllowedDomains(
                    DeityTools.DomainAllowed.ChaosDomainAllowed,
                    DeityTools.DomainAllowed.EvilDomainAllowed,
                    DeityTools.DomainAllowed.KnowledgeDomainAllowed,
                    DeityTools.DomainAllowed.MagicDomainAllowed,
